    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int botaoDialogo = JOptionPane.YES_NO_OPTION;
        JOptionPane.showConfirmDialog(null,"Você tem certeza? Essa operação pode ser PERMANENTE!","EXCLUIR VEICULO", botaoDialogo);
        if(botaoDialogo == JOptionPane.YES_OPTION){
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            int linhaSelecionadaIndex = jTable1.getSelectedRow();

            Peca pecaExcluir = new Peca();

            pecaExcluir.setCd_peca(Integer.parseInt(model.getValueAt(linhaSelecionadaIndex,0).toString()));
            pecaExcluir.setNome_peca(model.getValueAt(linhaSelecionadaIndex,1).toString());
            pecaExcluir.setQtd_peca(Integer.parseInt(model.getValueAt(linhaSelecionadaIndex,2).toString()));
            pecaExcluir.setValor_peca(Float.parseFloat(model.getValueAt(linhaSelecionadaIndex,3).toString()));
            pecaExcluir.setCD_VEIC_PECA(Integer.parseInt(model.getValueAt(linhaSelecionadaIndex,4).toString()));

            dao.delete(pecaExcluir);
            this.dispose();
            new ConsultaPeca().setVisible(true);

        }
        if(botaoDialogo == JOptionPane.NO_OPTION){
            remove(botaoDialogo);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btExcluirActionPerformed

