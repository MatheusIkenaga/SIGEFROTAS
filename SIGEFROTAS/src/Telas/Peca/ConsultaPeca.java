    public void AddRowToJTable(){
        
        DefaultTableModel model = new DefaultTableModel();
        Object[] columnsName = new Object [5];
        columnsName[0] = "Código";
        columnsName[1] = "Nome";
        columnsName[2] = "Quantidade";
        columnsName[3] = "Valor";
        columnsName[4] = "Veículo";
        
        model.setColumnIdentifiers(columnsName);
        
        List<Peca> list = dao.selectConsulta();
        
        Object rowData[] = new Object [5];
        for (int i = 0; i < dao.selectConsulta().size();i++) {
            rowData[0] = list.get(i).getCd_peca();
            rowData[1] = list.get(i).getNome_peca();
            rowData[2] = list.get(i).getQtd_peca();
            rowData[3] = list.get(i).getValor_peca();
            rowData[4] = veicDao.selectViagem(list.get(i).getCD_VEIC_PECA()).toString();
            
            model.addRow(rowData);
        }
            jTable1.setModel(model);
    }

