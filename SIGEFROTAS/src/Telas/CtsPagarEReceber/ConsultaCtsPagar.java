    public void AddRowToJTable(){
        DefaultTableModel model = new DefaultTableModel();
        
        jTable1.setDefaultEditor(Object.class, null);
        
        Object[] columnsName = new Object [8];
        columnsName[0] = "Código";
        columnsName[1] = "Valor da Conta";
        columnsName[2] = "Total Pago";
        columnsName[3] = "Status";
        columnsName[4] = "Data de Pagamento/Previsão";
        columnsName[5] = "Categoria";
        columnsName[6] = "Veículo";
        columnsName[7] = "Observações";
        
        model.setColumnIdentifiers(columnsName);
        
        List<ContaPagar> list = dao.selectConsulta();
        
        Object rowData[] = new Object [8];
        for (int i = 0; i < dao.selectConsulta().size();i++) {
            rowData[0] = list.get(i).getCD_CONTA();
            rowData[1] = list.get(i).getTOTAL_CONTA();
            rowData[2] = list.get(i).getTOTAL_PAGO();
            rowData[3] = list.get(i).getSTATUS_CONTA();
            
            //1997-08-21
            String diaPagto = list.get(i).getDT_PAGTO().substring(8,10);
            String mesPagto = list.get(i).getDT_PAGTO().substring(5,7);
            String anoPagto = list.get(i).getDT_PAGTO().substring(0,4);
            
            rowData[4] = diaPagto+"/"+mesPagto+"/"+anoPagto;
            rowData[5] = list.get(i).getCATEGORIA();
            rowData[6] = dao.selectVeicConta(list.get(i).getCD_CONTA());
            rowData[7] = list.get(i).getOBS_CONTA();

            model.addRow(rowData);
            
        }
    }

