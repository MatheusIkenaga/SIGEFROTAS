    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int botaoDialogo = JOptionPane.YES_NO_OPTION;
        JOptionPane.showConfirmDialog(null,"Você tem certeza? Essa operação pode ser PERMANENTE!","EXCLUIR VEICULO", botaoDialogo);
        if(botaoDialogo == JOptionPane.YES_OPTION){
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            int linhaSelecionadaIndex = jTable1.getSelectedRow();

            ContaPagar contaExcluir = new ContaPagar();

            contaExcluir.setCD_CONTA(Integer.parseInt(model.getValueAt(linhaSelecionadaIndex,0).toString()));
            contaExcluir.setTOTAL_CONTA(Float.parseFloat(model.getValueAt(linhaSelecionadaIndex,1).toString()));
            contaExcluir.setTOTAL_PAGO(Float.parseFloat(model.getValueAt(linhaSelecionadaIndex,2).toString()));
            contaExcluir.setSTATUS_CONTA(model.getValueAt(linhaSelecionadaIndex,3).toString());
            contaExcluir.setDT_PAGTO(model.getValueAt(linhaSelecionadaIndex,4).toString());
            contaExcluir.setCATEGORIA(model.getValueAt(linhaSelecionadaIndex,5).toString());
            contaExcluir.setCD_VEIC_CONTA(Integer.parseInt(dao.selectConta(contaExcluir.getCD_CONTA(),"CD_VEIC_CONTA")));
            contaExcluir.setOBS_CONTA(model.getValueAt(linhaSelecionadaIndex,7).toString());

            dao.delete(contaExcluir);
            this.dispose();
            new ConsultaCtsPagar().setVisible(true);

        }
        if(botaoDialogo == JOptionPane.NO_OPTION){
            remove(botaoDialogo);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btExcluirActionPerformed

